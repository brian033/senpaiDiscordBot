spclient = discord.Client()

async def background_task():
    await spclient.wait_until_ready()
    while not spclient.is_closed():
        w3 = Web3(Web3.HTTPProvider('https://mainnet.boba.network'))
        lp = Web3.toChecksumAddress('0x32fDfeA5CdCe7E417818ed5093E8bD4cA85dfE06')
        t1 = w3.eth.contract(Web3.toChecksumAddress('0xaC3a4aF1778203c8B651dAfA73cEd5b79c80F239'), abi = abiErc20)
        t2 = w3.eth.contract(Web3.toChecksumAddress('0xf74195Bb8a5cf652411867c5C2C5b8C2a402be35'), abi = abiErc20)
        decimals1 = t1.functions.decimals().call()
        decimals2 = t2.functions.decimals().call()
        b1 = t1.functions.balanceOf(lp).call() / (10**decimals1)
        b2 = t2.functions.balanceOf(lp).call() / (10**decimals2)
        print(f"Current Price of $SENPAI = {b2/b1} USD")
        name = "$"+str(round(b2/b1,2))
        await spclient.change_presence(activity=discord.Activity(type=discord.ActivityType.watching, name=name))
        await asyncio.sleep(3) 

@spclient.event
async def on_ready():
    print('We have logged in as {0.user}'.format(spclient))
    spclient.loop.create_task(background_task())

